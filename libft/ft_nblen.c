#include "libft.h"

int	ft_nblen(int n)
{
	int i;
	int t;

	i = 0;
	t = 0;
	if (n == 0)
		return (1);
	if (n == -2147483648)
		n = n + 1;
	if (n < 0)
	{
		i++;
		n = n * (-1);
	}
	while (n > 0)
	{
		n = n / 10;
		i++;
	}
	return (i);
}
