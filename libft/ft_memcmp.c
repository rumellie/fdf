#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t				i;
	const unsigned char *ts1;
	const unsigned char *ts2;

	i = 0;
	ts1 = (const unsigned char *)s1;
	ts2 = (const unsigned char *)s2;
	while (i < n)
	{
		if (ts1[i] != ts2[i])
			return (ts1[i] - ts2[i]);
		i++;
	}
	return (0);
}
